package day_03;

public class Swap_without_temo {

	public static void main(String[] args) {
		// before swaping
		int a=38;
		int b=29;
		System.out.println("a is:"+a);
		System.out.println("b is:"+b);
		//After swap
		 a=a+b;
		 b=a-b;
	     a=a-b;
		System.out.println("a is:"+a);
		System.out.println("b is:"+b);
		

	}

}
