package day_02;

public class ComparisonOperators {

	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println("The equal of num is:"+(a==b));
		System.out.println("The greater of num is:"+(a>b));
		System.out.println("The lesser of num is:"+(a<b));
		System.out.println("The greater or equall of num is:"+(a>=b));
		System.out.println("The lesser or equal of num is:"+(a<=b));
		System.out.println("The not equal to of num is:"+(a!=b));
		
		

	}

}
