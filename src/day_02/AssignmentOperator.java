package day_02;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println("The equal of num is:"+(a=b));
		System.out.println("The add of num is:"+(a+=b));
		System.out.println("The sub of num is:"+(a-=b));
		System.out.println("The mul of num is:"+(a*=b));
		System.out.println("The div of num is:"+(a/=b));
		System.out.println("The mudulo of num is:"+(a%=b));

	}

}
